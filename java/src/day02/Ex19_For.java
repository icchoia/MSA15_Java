package day02;

public class Ex19_For {
	public static void main(String[] args) {
		// for ( 1초기식; 2조건식; 4증감식) { 3실행문}
		// 실행순서 : 1 -> 반복 (2 -> 3-> 4)
		
		// 1. 1~ 10까지 정수를 출력하세요.
		for (int i = 0; i <= 10; i++) {
			System.out.print(i + " ");
		}
		System.out.println();
		
		// 2. 50~ 100까지 정수를 출력하세요.
		for (int i = 50; i <= 100; i++) {
			System.out.print(i + " ");
		}
		System.out.println();
	}
}
